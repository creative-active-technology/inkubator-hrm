	@Override
	@Transactional(readOnly = true, isolation = Isolation.REPEATABLE_READ, propagation = Propagation.SUPPORTS, timeout = 50)
	public List<UnregPayComponents> getByParam(UnregPayComponentsSearchParameter searchParameter, int firstResult, int maxResults, Order order) {
		List<UnregPayComponents> list = unregPayComponentsDao.getByParam(searchParameter, firstResult, maxResults, order);
		for(UnregPayComponents unregPayComponents : list){
			Set<UnregPayComponentsException> exceptions = unregPayComponents.getUnregPayComponentsExceptions();
			unregPayComponents.setTotalEmpData(exceptions.size());
			unregPayComponents.setTotalNominal(Lambda.sum(exceptions, Lambda.on(UnregPayComponentsException.class).getNominal()));
		}		
		return list;
	}

