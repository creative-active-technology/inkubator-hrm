    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(TravelZone b) throws Exception {
        // check duplicate name
        long totalDuplicates = travelZoneDao.getTotalByCodeAndNotId(b.getCode(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("travelZone.error_duplicate_travelZone_code");
        }

        TravelZone travelZone = travelZoneDao.getEntiyByPK(b.getId());
        travelZone.setCode(b.getCode());
        travelZone.setName(b.getName());
        travelZone.setCategory(b.getCategory());
        travelZone.setDistance(b.getDistance());
        travelZone.setDescription(b.getDescription());
        travelZone.setUpdatedBy(UserInfoUtil.getUserName());
        travelZone.setUpdatedOn(new Date());
        travelZoneDao.update(travelZone);
    }

