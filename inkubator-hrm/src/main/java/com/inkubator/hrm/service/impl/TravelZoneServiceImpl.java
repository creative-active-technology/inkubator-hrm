    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(TravelZone travelZone) throws Exception {
        // check duplicate name
        long totalDuplicates = travelZoneDao.getTotalByCode(travelZone.getCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("travelZone.error_duplicate_travelZone_code");
        }

        travelZone.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        travelZone.setCreatedBy(UserInfoUtil.getUserName());
        travelZone.setCreatedOn(new Date());
        travelZoneDao.save(travelZone);
    }

