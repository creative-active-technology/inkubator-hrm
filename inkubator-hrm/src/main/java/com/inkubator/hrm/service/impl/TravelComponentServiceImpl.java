    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(TravelComponent travelComponent) throws Exception {
        // check duplicate name
        long totalDuplicates = travelComponentDao.getTotalByCode(travelComponent.getCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("travelComponent.error_duplicate_travelComponent_code");
        }

        travelComponent.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        travelComponent.setCreatedBy(UserInfoUtil.getUserName());
        travelComponent.setCreatedOn(new Date());
        travelComponentDao.save(travelComponent);
    }

