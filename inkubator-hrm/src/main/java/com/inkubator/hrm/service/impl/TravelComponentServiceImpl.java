    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(TravelComponent b) throws Exception {
        // check duplicate name
        long totalDuplicates = travelComponentDao.getTotalByCodeAndNotId(b.getCode(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("travelComponent.error_duplicate_travelComponent_code");
        }

        TravelComponent travelComponent = travelComponentDao.getEntiyByPK(b.getId());
        travelComponent.setCode(b.getCode());
        travelComponent.setName(b.getName());
        travelComponent.setMeasurement(b.getMeasurement());
        travelComponent.setDescription(b.getDescription());
        travelComponent.setUpdatedBy(UserInfoUtil.getUserName());
        travelComponent.setUpdatedOn(new Date());
        travelComponentDao.update(travelComponent);
    }

