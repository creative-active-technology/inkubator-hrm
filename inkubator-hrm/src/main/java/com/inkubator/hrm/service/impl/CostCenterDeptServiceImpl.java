	public void save(CostCenterDept entity) throws Exception {
		// check duplicate code
        long totalDuplicates = costCenterDeptDao.getTotalByCode(entity.getCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("costcenter.error_duplicate_cost_center_code");
        }
        
        entity.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        entity.setCreatedBy(UserInfoUtil.getUserName());
        entity.setCreatedOn(new Date());
        this.costCenterDeptDao.save(entity);

	}

	@Override
	@Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor =Exception.class)
	public void update(CostCenterDept entity) throws Exception {
		// check duplicate code
        long totalDuplicates = costCenterDeptDao.getTotalByCodeAndNotId(entity.getCode(), entity.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("costcenter.error_duplicate_cost_center_code");
        }
        
        CostCenterDept costCenterDept = costCenterDeptDao.getEntiyByPK(entity.getId());
        costCenterDept.setCode(entity.getCode());
        costCenterDept.setName(entity.getName());
        costCenterDept.setDescription(entity.getDescription());
        costCenterDept.setUpdatedBy(UserInfoUtil.getUserName());
        costCenterDept.setUpdatedOn(new Date());
        this.costCenterDeptDao.save(costCenterDept);

	}

