    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(OccupationType r) throws Exception {
        // check duplicate name
        long totalDuplicates = occupationTypeDao.getTotalByNameAndNotId(r.getOccupationTypeName(), r.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("occupationType.error_duplicate_occupationType_name");
        }

        OccupationType occupationType = occupationTypeDao.getEntiyByPK(r.getId());
        occupationType.setOccupationTypeName(r.getOccupationTypeName());
        occupationType.setDescription(r.getDescription());
        occupationType.setUpdatedBy(UserInfoUtil.getUserName());
        occupationType.setUpdatedOn(new Date());
        occupationTypeDao.update(occupationType);
    }

