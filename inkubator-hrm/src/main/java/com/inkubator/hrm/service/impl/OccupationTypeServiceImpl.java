    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(OccupationType occupationType) throws Exception {
        // check duplicate name
        long totalDuplicates = occupationTypeDao.getTotalByName(occupationType.getOccupationTypeName());
        if (totalDuplicates > 0) {
            throw new BussinessException("occupationType.error_duplicate_occupationType_name");
        }

        occupationType.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        occupationType.setCreatedBy(UserInfoUtil.getUserName());
        occupationType.setCreatedOn(new Date());
        occupationTypeDao.save(occupationType);
    }

