    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(BioInsurance bioInsurance) throws Exception {
        // check duplicate name
        long totalDuplicates = bioInsuranceDao.getTotalByNoPolicy(bioInsurance.getNoPolicy());
        if (totalDuplicates > 0) {
            throw new BussinessException("bioInsurance.error_duplicate_bioInsurance_noPolicy");
        }
        BioData biodata = bioDataDao.getEntiyByPK(bioInsurance.getBioData().getId());

        bioInsurance.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        bioInsurance.setBioData(biodata);
        bioInsurance.setCreatedBy(UserInfoUtil.getUserName());
        bioInsurance.setCreatedOn(new Date());
        bioInsuranceDao.save(bioInsurance);
    }

