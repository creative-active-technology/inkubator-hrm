    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(BioInsurance b) throws Exception {
        // check duplicate name
        long totalDuplicates = bioInsuranceDao.getTotalByNoPolicyAndNotId(b.getNoPolicy(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("bioInsurance.error_duplicate_bioInsurance_noPolicy");
        }
        BioData biodata = bioDataDao.getEntiyByPK(b.getBioData().getId());
        BioInsurance bioInsurance = bioInsuranceDao.getEntiyByPK(b.getId());
        bioInsurance.setBioData(biodata);
        bioInsurance.setNoPolicy(b.getNoPolicy());
        bioInsurance.setInstance(b.getInstance());
        bioInsurance.setFacility(b.getFacility());
        bioInsurance.setDescription(b.getDescription());
        bioInsurance.setUpdatedBy(UserInfoUtil.getUserName());
        bioInsurance.setUpdatedOn(new Date());
        bioInsuranceDao.update(bioInsurance);
    }

