    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(Dialect dialect) throws Exception {
        // check duplicate name
        long totalDuplicates = dialectDao.getTotalByCode(dialect.getDialectCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("dialect.error_duplicate_dialect_code");
        }

        dialect.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        dialect.setCreatedBy(UserInfoUtil.getUserName());
        dialect.setCreatedOn(new Date());
        dialectDao.save(dialect);
    }

