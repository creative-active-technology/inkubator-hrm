    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(Dialect b) throws Exception {
        // check duplicate name
        long totalDuplicates = dialectDao.getTotalByCodeAndNotId(b.getDialectCode(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("dialect.error_duplicate_dialect_code");
        }

        Dialect dialect = dialectDao.getEntiyByPK(b.getId());
        dialect.setDialectCode(b.getDialectCode());
        dialect.setDialectName(b.getDialectName());
        dialect.setDescription(b.getDescription());
        dialect.setUpdatedBy(UserInfoUtil.getUserName());
        dialect.setUpdatedOn(new Date());
        dialectDao.update(dialect);
    }

