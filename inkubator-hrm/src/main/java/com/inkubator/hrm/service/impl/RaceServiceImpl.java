    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(Race race) throws Exception {
        // check duplicate name
        long totalDuplicates = raceDao.getTotalByCode(race.getRaceCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("race.error_duplicate_race_code");
        }

        race.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        race.setCreatedBy(UserInfoUtil.getUserName());
        race.setCreatedOn(new Date());
        raceDao.save(race);
    }

