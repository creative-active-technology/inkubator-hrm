    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(Race b) throws Exception {
        // check duplicate name
        long totalDuplicates = raceDao.getTotalByCodeAndNotId(b.getRaceCode(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("race.error_duplicate_race_code");
        }

        Race race = raceDao.getEntiyByPK(b.getId());
        race.setRaceCode(b.getRaceCode());
        race.setRaceName(b.getRaceName());
        race.setDescription(b.getDescription());
        race.setUpdatedBy(UserInfoUtil.getUserName());
        race.setUpdatedOn(new Date());
        raceDao.update(race);
    }

