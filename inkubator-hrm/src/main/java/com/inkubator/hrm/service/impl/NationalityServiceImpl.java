    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void save(Nationality nationality) throws Exception {
        // check duplicate name
        long totalDuplicates = nationalityDao.getTotalByCode(nationality.getNationalityCode());
        if (totalDuplicates > 0) {
            throw new BussinessException("nationality.error_duplicate_nationality_code");
        }

        nationality.setId(Long.parseLong(RandomNumberUtil.getRandomNumber(9)));
        nationality.setCreatedBy(UserInfoUtil.getUserName());
        nationality.setCreatedOn(new Date());
        nationalityDao.save(nationality);
    }

