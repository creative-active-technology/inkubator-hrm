    @Override
    @Transactional(readOnly = false, isolation = Isolation.READ_COMMITTED, propagation = Propagation.REQUIRED, rollbackFor = Exception.class)
    public void update(Nationality b) throws Exception {
        // check duplicate name
        long totalDuplicates = nationalityDao.getTotalByCodeAndNotId(b.getNationalityCode(), b.getId());
        if (totalDuplicates > 0) {
            throw new BussinessException("nationality.error_duplicate_nationality_code");
        }

        Nationality nationality = nationalityDao.getEntiyByPK(b.getId());
        nationality.setNationalityCode(b.getNationalityCode());
        nationality.setNationalityName(b.getNationalityName());
        nationality.setDescription(b.getDescription());
        nationality.setUpdatedBy(UserInfoUtil.getUserName());
        nationality.setUpdatedOn(new Date());
        nationalityDao.update(nationality);
    }

